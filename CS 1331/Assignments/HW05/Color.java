/**
* Creates enum Color.
*
* @author iiisong
* @version 1.0.0
*/
public enum Color {
    RED,
    GREEN,
    BLUE,
    YELLOW,
    MAGENTA,
    CYAN,
    BLACK,
    DARKGRAY,
    LIGHTGRAY,
    BROWN,
    BLONDE,
    WHITE;

    @Override
    public String toString() {
        return super.toString().toLowerCase();
    }
}
